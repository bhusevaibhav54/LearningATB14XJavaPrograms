package ex_04_Operators;

public class Lab033_Operators_Relational {
    public static void main(String[] args) {
//        <   Less Than
//        <=  Less Than or Equals to
//        >   Greater Than
//        >=  Greater Than or Equals to
//        ==  equals to (but checking)
//        !=  not Equals to

        int a = 10;
        int b = 30;

        boolean c = a > b;
        System.out.println(c);

        int age_mamitha = 33;
        int age_promod = 34;

        boolean result = age_promod >= age_mamitha;
        System.out.println(result);
    }
}
