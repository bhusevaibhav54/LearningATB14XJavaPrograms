package Exercise.Exercise_26thOct.ForLoop;

public class Sum_first_10_Natural_number {
    public static void main(String[] args) {
        int sum =0;
        for(int i=1;i<=10;i++)
        {
            sum=sum+i;
        }
        System.out.println("Sum of 1st 10 natural number: "+ sum);
    }
}
